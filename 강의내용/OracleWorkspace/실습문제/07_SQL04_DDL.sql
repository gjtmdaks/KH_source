-- 11.
DROP VIEW VW_지도면담;

CREATE VIEW VW_지도면담
AS SELECT STUDENT_NAME 학생이름, DEPARTMENT_NAME 학과이름, NVL(PROFESSOR_NAME,'지도교수없음') 지도교수이름
    FROM TB_STUDENT
    JOIN TB_DEPARTMENT USING(DEPARTMENT_NO)
    LEFT JOIN TB_PROFESSOR ON PROFESSOR_NO = COACH_PROFESSOR_NO
    ORDER BY 2;
    
SELECT * FROM VW_지도면담;

