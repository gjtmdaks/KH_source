SELECT CLASS_NO, CLASS_NAME, COUNT(*)
FROM TB_CLASS
JOIN TB_GRADE USING(CLASS_NO)
WHERE 2009 - SUBSTR(TERM_NO,1,4) < 5
GROUP BY CLASS_NO, CLASS_NAME
ORDER BY 3 DESC;

SELECT CLASS_NO, CLASS_NAME, "누적수강생수(명)"
FROM (SELECT CLASS_NO, CLASS_NAME, COUNT(*) "누적수강생수(명)"
    FROM TB_CLASS
    JOIN TB_GRADE USING(CLASS_NO)
    WHERE TERM_NO >= (SUBSTR(ADD_MONTHS(SYSDATE, -60),1,4))
    GROUP BY CLASS_NO, CLASS_NAME
    ORDER BY 3 DESC)
WHERE ROWNUM <=3;

SELECT *
FROM (SELECT CLASS_NO, CLASS_NAME, COUNT(*) "누적수강생수(명)"
    FROM TB_CLASS
    JOIN TB_GRADE USING(CLASS_NO)
    WHERE 2009 - SUBSTR(TERM_NO,1,4) < 5
    GROUP BY CLASS_NO, CLASS_NAME
    ORDER BY 3 DESC)
WHERE ROWNUM <=3;
