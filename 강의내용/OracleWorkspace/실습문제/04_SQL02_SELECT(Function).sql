-- 7.
-- TO_DATE('99/10/11','YY/MM/DD') -> 2099년 10월 11일
-- TO_DATE('49/10/11','YY/MM/DD') -> 2049년 10월 11일
-- TO_DATE('99/10/11','RR/MM/DD') -> 1999년 10월 11일
-- TO_DATE('49/10/11','RR/MM/DD') -> 2049년 10월 11일
SELECT TO_CHAR(TO_DATE('99/10/11','YY/MM/DD'), 'YYYY/MM/DD') A,
        TO_CHAR(TO_DATE('49/10/11','YY/MM/DD'), 'YYYY/MM/DD') B,
        TO_CHAR(TO_DATE('99/10/11','RR/MM/DD'), 'RRRR/MM/DD') C,
        TO_CHAR(TO_DATE('49/10/11','RR/MM/DD'), 'RRRR/MM/DD') D
FROM DUAL;

